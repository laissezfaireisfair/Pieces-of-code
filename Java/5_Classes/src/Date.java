public class Date {
	private final int m_year;
	private final int m_mounth;
	private final int m_day;
	Date(int year, int month, int day) {
		m_year = year;
		m_mounth = mounth;
		m_day = day;
	}
	public year() {
		return m_year;
	}
	public month() {
		return m_mounth;
	}
	public day() {
		return m_day;
	}
}